async function getLiveMarketSignal() {
    // यह हिस्सा इंटरनेट से डेटा 'याचना' (Request) करता है
    try {
        // हम 'Nifty 50' और 'SBI Funds' की लहरों को देख रहे हैं
        let market_mood = "Fearful"; // मान लीजिए आज बाज़ार में डर है
        
        if (market_mood === "Fearful") {
            return "॥ विशेष सिग्नल ॥: सोनू भाई, बाज़ार में गिरावट है। ₹2,500 के अलावा थोड़ा और निवेश का मुहूर्त है!";
        } else {
            return "॥ शुभ स्थिति ॥: आपकी ₹2,500 की SIP सुरक्षित रूप से बढ़ रही है।";
        }
    } catch (error) {
        return "॥ मौन ॥: प्रहरी अभी ध्यान मुद्रा में है।";
    }
}
