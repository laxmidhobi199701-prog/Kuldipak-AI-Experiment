const Sanskriti = {
    // १. पवित्र विचारों का संग्रह (Daily Inspiration)
    vichar: [
        "शुचिता ही धन की असली लक्ष्मी है।",
        "प्रतीक्षा और नीति का भविष्य, आज के संयम में है।",
        "भीलवाड़ा की मिट्टी का संकल्प, २०४७ की सिद्धि।",
        "निवेश केवल धन नहीं, आने वाली पीढ़ी का विश्वास है।",
        "बाज़ार की लहरें अस्थाई हैं, आपका लक्ष्य अटल है।"
    ],

    // २. रूहानी संकेत प्राप्त करना
    getPrerna: function() {
        const index = new Date().getDate() % this.vichar.length;
        return this.vichar[index];
    }
};

console.log("॥ हृदय संदेश ॥: " + Sanskriti.getPrerna());
